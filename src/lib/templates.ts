// Get templates by category
export function getTemplatesByCategory(category: AdTemplate['category']): AdTemplate[] {
  return adTemplates.filter(t => t.category === category);
}

// Get templates by size
export function getTemplatesBySize(width: number, height: number): AdTemplate[] {
  return adTemplates.filter(t => t.width === width && t.height === height);
}

// Get all categories
export function getCategories(): AdTemplate['category'][] {
  return ['promotion', 'product', 'seasonal', 'clubcard', 'awareness'];
}

